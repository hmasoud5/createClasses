public class Multiply 
{
// attributes--variables
  private int num1;
  private int num2;

  // constructor --assigns
  public Multiply(int number1, int number2)
  {
    num1 = number1;
    num2 = number2;
  }
  //behaviors--methods
  public int MultiplyOf()
  {
  return num1 * num2; 
   //num1 multiplied by num2
  }
}